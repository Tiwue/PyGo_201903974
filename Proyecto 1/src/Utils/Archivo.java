package Utils;


/**
 *
 * @author steve
 */
public class Archivo {
    String ruta, nombre;
    public Archivo(String ruta, String nombre){
        setNombre(nombre);
        setRuta(ruta);
    }

    public String getRuta() {
        return ruta;
    }

    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
